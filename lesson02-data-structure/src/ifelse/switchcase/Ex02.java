package ifelse.switchcase;

public class Ex02 {
	public static void main(String[] args) {
		int number = 3;
		switch (number) {
		case 1:
			System.out.println("one");
			break;
		case 2:
			System.out.println("two");
			break;
		case 3:
			System.out.println("three");
			break;
		case 4:
			System.out.println("four");
			break;
		default:
			break;
		}
	}

}
