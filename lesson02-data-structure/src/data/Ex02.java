package data;

public class Ex02 {
	// global variable: attributre
	static int year = 2021;
	
	// function: method
	public static void main(String[] args) {
		
		int year = 1998;
		int age = 20;
		int salary = 20;
		age = 21;
		salary = 22;
		System.out.println("age: " + age);
		System.out.println("salary: " + salary);
		age = salary; // assign salary value to age
		increase(salary);
		System.out.println("salary: " + salary);
		salary = 30;
		swap(salary, age); // salary = 30;age = 22;
		System.out.println("age: " + age);
		System.out.println("salary: " + salary);
	
		
		
	}
	//a=5 b=7
	private static void swap(int a,int b) {
		int tmp = a;
		a = b;
		b = tmp;
		
		// alt left || right: shift to previous|next cursor
		System.out.println("Year swap: " + year);
	}
	private static void increase(int input) {
		// calculation
		input = 30;
	}
}